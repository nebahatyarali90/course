package day15;

public class task1 {
    public static void main(String[] args) {
        // print grid of "#" with 5 rows and 5 columns
        // using nested loop

//            #####
//            #####
//            #####
//            #####
//            #####
        // hint: print, println



            for(int row = 0; row < 5; row++) { // row

                for(int col = 0; col < 5; col++) { // column
                    System.out.print( "#" );
                }

                System.out.println();
            }


        }


    }













