package Day2;

public class JavaEscapeCharacter {
    public static void main(String[] args) {
        // new line -> \n
        System.out.println("Techno \n Study");

        // quote -> \"

        System.out.println("Hi\" ");

        // tabulation  \t

        System.out.println("\tHello");

        //backslash --> \\
        System.out.println("\\");
    }
}
