package day2;

public class javaEscapeCaracters {
    public static void main(String[] args) {

        //new line -> \n
        System.out.println("Techno \n Study");


        // quote -> \"
        System.out.println("  \"Hi\" ");

        // tabulation \t
        System.out.println("\t Hello");

        // backsplash ->//
        System.out.println("\\");


    }
}
